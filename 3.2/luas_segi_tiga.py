# Pengguna masukkan ukuran panjang tapak
tapak = float(input("Masukkan ukuran panjang tapak (cm): "))

# Pengguna masukkan ukuran tinggi
tinggi = float(input("Masukkan ukuran tinggi (cm): "))

# Formula mengira luas segi tiga
luas = 0.5 * tapak * tinggi

# Mencetak output
print("Luas segi tiga ialah", luas)
